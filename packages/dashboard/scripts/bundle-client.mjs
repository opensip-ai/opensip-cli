/**
 * Bundle the dashboard client modules (src/client/*.ts) into ONE deterministic
 * IIFE string and emit it as a TS const (src/client-bundle.generated.ts) that
 * generator.ts inlines into the report's <script> block (L4 migration).
 *
 * Deterministic (no sourcemap, no timestamps, no legal comments) so the golden /
 * snapshot tests stay stable. Runs after the client type-check (`tsc -p
 * src/client/tsconfig.json`) and before the package `tsc` (which compiles the
 * generated const). esbuild only transpiles/bundles — type-safety is the tsc
 * step's job.
 */

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { build } from 'esbuild';

const pkgDir = dirname(dirname(fileURLToPath(import.meta.url))); // packages/dashboard

const result = await build({
  entryPoints: [join(pkgDir, 'src/client/index.ts')],
  bundle: true,
  format: 'iife',
  target: 'es2022',
  write: false,
  sourcemap: false,
  legalComments: 'none',
});

const code = result.outputFiles[0].text;
const out = join(pkgDir, 'src/client-bundle.generated.ts');
writeFileSync(
  out,
  `// @generated by scripts/bundle-client.mjs from src/client/*.ts — DO NOT EDIT.\n` +
    `export const DASHBOARD_CLIENT_BUNDLE: string = ${JSON.stringify(code)};\n`,
);
console.log(`[bundle-client] wrote src/client-bundle.generated.ts (${code.length} bytes)`);
