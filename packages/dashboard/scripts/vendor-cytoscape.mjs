#!/usr/bin/env node
/**
 * Vendor the Cytoscape renderer stack into a single committed UMD bundle.
 *
 * Reads the minified UMD globals for `cytoscape` and `cytoscape-dagre`
 * from `node_modules`, concatenates them with version-stamp headers, and
 * writes `src/vendor/cytoscape-bundle.js`. That file is committed and
 * treated as source for code-review purposes — the diff is auditable when
 * versions bump.
 *
 * Why these two and not three: `cytoscape-dagre@3` bundles its own dagre
 * (`@dagrejs/dagre`) inside its UMD build, so the standalone `dagre`
 * package is dead weight (~280 KB) and is deliberately NOT vendored. See
 * Phase 0 of docs/plans/ready/graph-visualizer-view.
 *
 * Why `cytoscape.min.js` (minified UMD) and not `cytoscape.umd.js`
 * (unminified, ~1.17 MB): minified is the offline-distributable global
 * build. The unminified UMD blows the budget on its own.
 *
 * The script fails (non-zero exit) if the raw bundle exceeds BUDGET_KB so a
 * version bump that bloats the renderer is caught at vendor time, not in
 * the report. The Phase 6 snapshot test is the second gate (in CI, without
 * node_modules present).
 *
 * Run: `pnpm --filter=@opensip-tools/dashboard vendor:cytoscape`
 */

import { gzipSync } from 'node:zlib';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const HERE = dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = join(HERE, '..');

/** Raw-byte budget. Phase 0 measured ~480 KB; 600 KB allows modest growth. */
const BUDGET_KB = 600;

/** Resolve a package's version without tripping its `exports` map. */
function pkgVersion(name) {
  const entry = require.resolve(name);
  // Walk up to the package.json next to the package's node_modules dir.
  let dir = dirname(entry);
  for (let i = 0; i < 8; i++) {
    try {
      const pj = JSON.parse(readFileSync(join(dir, 'package.json'), 'utf8'));
      if (pj.name === name) return pj.version;
    } catch {
      // keep walking
    }
    dir = dirname(dir);
  }
  return 'unknown';
}

function resolveFromPackage(pkg, relPath) {
  // require.resolve(pkg) may hit the package's exports map and miss dist
  // files; resolve the package dir from its own package.json instead.
  const main = require.resolve(pkg);
  let dir = dirname(main);
  for (let i = 0; i < 8; i++) {
    try {
      const pj = JSON.parse(readFileSync(join(dir, 'package.json'), 'utf8'));
      if (pj.name === pkg) return join(dir, relPath);
    } catch {
      // keep walking
    }
    dir = dirname(dir);
  }
  throw new Error(`could not locate ${pkg} package root`);
}

const sources = [
  {
    name: 'cytoscape',
    version: pkgVersion('cytoscape'),
    file: resolveFromPackage('cytoscape', 'dist/cytoscape.min.js'),
    global: 'cytoscape',
  },
  {
    name: 'cytoscape-dagre',
    version: pkgVersion('cytoscape-dagre'),
    file: resolveFromPackage('cytoscape-dagre', 'cytoscape-dagre.js'),
    global: 'cytoscapeDagre',
  },
];

const banner =
  '/*\n' +
  ' * VENDORED — DO NOT EDIT BY HAND.\n' +
  ' * Generated by packages/dashboard/scripts/vendor-cytoscape.mjs.\n' +
  ' * Regenerate with: pnpm --filter=@opensip-tools/dashboard vendor:cytoscape\n' +
  ' *\n' +
  ' * Bundled UMD globals (offline-capable, inlined into every report):\n' +
  sources.map(s => ` *   - ${s.name}@${s.version}  → global "${s.global}"\n`).join('') +
  ' *\n' +
  ' * cytoscape-dagre@3 self-bundles @dagrejs/dagre; no standalone dagre is\n' +
  ' * vendored. Layout registration happens at view init via\n' +
  ' * `cytoscape.use(cytoscapeDagre)`.\n' +
  ' */\n';

const parts = [banner];
for (const s of sources) {
  parts.push(`\n/* ===== ${s.name}@${s.version} ===== */\n`);
  parts.push(readFileSync(s.file, 'utf8'));
  parts.push('\n');
}
const bundle = parts.join('');

const rawBytes = Buffer.byteLength(bundle, 'utf8');
const gzBytes = gzipSync(bundle).length;
const rawKb = Math.round(rawBytes / 1024);
const gzKb = Math.round(gzBytes / 1024);

const outDir = join(PKG_ROOT, 'src', 'vendor');
mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, 'cytoscape-bundle.js');
writeFileSync(outFile, bundle);

console.log(`vendored cytoscape bundle → ${outFile}`);
for (const s of sources) console.log(`  ${s.name}@${s.version}`);
console.log(`  raw:  ${rawBytes} bytes (${rawKb} KB)`);
console.log(`  gzip: ${gzBytes} bytes (${gzKb} KB)`);
console.log(`  budget: ${BUDGET_KB} KB raw`);

if (rawKb > BUDGET_KB) {
  console.error(
    `\nERROR: vendor bundle ${rawKb} KB exceeds the ${BUDGET_KB} KB budget. ` +
      `Either shrink the renderer stack or raise BUDGET_KB with justification.`,
  );
  process.exit(1);
}
