#!/usr/bin/env node
/**
 * build-package-readmes — generate a per-package README.md for every
 * published workspace package from a single template + package.json metadata.
 *
 * Why: npm renders each package's page from the README *inside that package's
 * own tarball*. The monorepo root README is never bundled into a sub-package,
 * so without this every `@opensip-cli/*` page reads "This package does not
 * have a README file" — looks abandoned and hurts search ranking.
 *
 * Single source of truth: each package.json's own fields (`name`,
 * `description`, `repository.directory`, `license`, `opensipTools.kind`). No
 * second list to drift. The role sentence is derived from the `kind` marker
 * (tool / fit-pack / graph-adapter / internal lib), so adding a package needs
 * no edit here — just a `description` and (if it's a plugin) its kind marker.
 *
 * The unscoped `opensip-cli` CLI is EXCLUDED: its README is the curated
 * product front page, hand-authored, not generated.
 *
 * Usage:
 *   node scripts/build-package-readmes.mjs           # write READMEs
 *   node scripts/build-package-readmes.mjs --check    # exit 1 if any are stale
 *
 * Mirrors the build-web-docs.mjs generator + `--check` staleness-gate pattern.
 * Generated files are committed (so they ship in the tarball and reviewers see
 * them); CI runs `--check` to keep them in sync with package.json.
 */

import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_OWNER = 'opensip-ai';
const REPO_NAME = 'opensip-cli';
const DOCS_SITE_URL = 'https://opensip.ai/docs/opensip-cli/';
const CLI_PACKAGE_NAME = 'opensip-cli'; // curated README — never generated

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = dirname(dirname(__filename));
const PACKAGES_DIR = join(REPO_ROOT, 'packages');
const CHECK_ONLY = process.argv.slice(2).includes('--check');

const log = (msg) => console.error(`[build-package-readmes] ${msg}`);

const SKIP_DIRS = new Set(['node_modules', 'dist', '.git', '.claude', 'coverage']);

/** Recursively collect every package.json path under packages/. */
function collectPackageJsonPaths(dir, out) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (SKIP_DIRS.has(entry.name)) continue;
      collectPackageJsonPaths(join(dir, entry.name), out);
    } else if (entry.name === 'package.json') {
      out.push(join(dir, entry.name));
    }
  }
}

/** The release ref the README's source links pin to (e.g. `v1.0.0`). */
function readReleaseRef() {
  const pkg = JSON.parse(readFileSync(join(REPO_ROOT, 'packages/core/package.json'), 'utf8'));
  return `v${pkg.version}`;
}

/** One-line structural role, keyed by the opensipTools.kind marker. */
const ROLE_BY_KIND = {
  tool: 'This package is the **engine** behind a first-party `opensip-cli` tool. The CLI bundles it — you do not normally install it directly.',
  'fit-pack':
    'This is a **fitness check pack**. Install it alongside the `opensip-cli` CLI and its checks are discovered automatically.',
  'graph-adapter':
    'This is a **graph language adapter**. Install it alongside the CLI to add language support to `opensip graph`.',
};

const DEFAULT_ROLE =
  'This is an **internal library** of the opensip-cli toolkit. It is published so the CLI and tools can depend on it; most users will not install it directly.';

function roleSentence(kind) {
  return ROLE_BY_KIND[kind] ?? DEFAULT_ROLE;
}

const GENERATED_MARKER =
  '<!-- Generated by scripts/build-package-readmes.mjs — do not edit by hand.\n' +
  '     Change the package\'s "description" (or the generator template) and run\n' +
  '     `pnpm docs:readmes`. CI enforces sync via `pnpm docs:readmes:check`. -->';

/** Build the full README text for one package. Deterministic. */
function renderReadme(pkg, releaseRef) {
  const { name, description, license = 'MIT', repository } = pkg;
  const dir = repository?.directory;
  const repoRootUrl = `https://github.com/${REPO_OWNER}/${REPO_NAME}`;
  const sourceUrl = dir ? `${repoRootUrl}/tree/${releaseRef}/${dir}` : repoRootUrl;
  const catalogUrl = `${repoRootUrl}/blob/${releaseRef}/docs/public/70-reference/02-package-catalog.md`;

  return (
    `${GENERATED_MARKER}\n\n` +
    `# ${name}\n\n` +
    `> ${description}\n\n` +
    `${roleSentence(pkg.opensipTools?.kind)}\n\n` +
    `Part of [**opensip-cli**](${repoRootUrl}) — an open-source codebase-analysis ` +
    `toolkit: fitness checks (\`fit\`), static call-graph analysis (\`graph\`), and ` +
    `simulation (\`sim\`).\n\n` +
    `## Install\n\n` +
    'Most users install the CLI, which bundles the first-party tools:\n\n' +
    '```bash\n' +
    'curl -fsSL https://opensip.ai/cli/install.sh | bash\n' +
    '```\n\n' +
    `This package is published for the CLI and advanced plugin authors; most ` +
    `users should not install \`${name}\` directly.\n\n` +
    `## Documentation\n\n` +
    `- 📚 Project docs: ${DOCS_SITE_URL}\n` +
    `- 🧭 Package catalog (what every package does): ${catalogUrl}\n` +
    `- 📦 Source: ${sourceUrl}\n\n` +
    `## License\n\n` +
    `${license} © ${REPO_OWNER}\n`
  );
}

function main() {
  const releaseRef = readReleaseRef();
  const paths = [];
  collectPackageJsonPaths(PACKAGES_DIR, paths);
  paths.sort();

  const stale = [];
  let written = 0;
  let considered = 0;

  for (const pjPath of paths) {
    const pkg = JSON.parse(readFileSync(pjPath, 'utf8'));
    // Releasable @opensip-cli/* packages only; skip private + the curated CLI.
    if (typeof pkg.name !== 'string') continue;
    if (!pkg.name.startsWith('@opensip-cli/')) continue;
    if (pkg.name === CLI_PACKAGE_NAME) continue;
    if (pkg.private === true) continue;
    if (!pkg.description) {
      log(`WARNING: ${pkg.name} has no "description" — skipping (add one to include it).`);
      continue;
    }

    considered++;
    const readmePath = join(dirname(pjPath), 'README.md');
    const next = renderReadme(pkg, releaseRef);
    const current = existsSync(readmePath) ? readFileSync(readmePath, 'utf8') : null;

    if (current === next) continue;

    if (CHECK_ONLY) {
      stale.push(relative(REPO_ROOT, readmePath));
    } else {
      writeFileSync(readmePath, next, 'utf8');
      written++;
    }
  }

  if (CHECK_ONLY) {
    if (stale.length === 0) {
      log(`all ${considered} package README(s) in sync (ref ${releaseRef}).`);
      return;
    }
    log(`${stale.length} package README(s) stale — run \`pnpm docs:readmes\` and commit:`);
    for (const f of stale) log(`  - ${f}`);
    process.exit(1);
  }

  log(`considered ${considered} package(s); wrote ${written} README(s) (ref ${releaseRef}).`);
}

main();
